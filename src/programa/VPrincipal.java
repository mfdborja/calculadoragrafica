    //Forma de detectar el Control+C y Control+V
    //Para comprobar si han pulsado control + algo
    boolean controlPulsado = false;
    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        //Para control+c y control+v
        if (evt.getKeyCode() == KeyEvent.VK_CONTROL) {//Valor para la tecla control
            //Si la tecla que se detecta es control. Se activa el boleano
            controlPulsado = true;
        } else {
            //Si no es control será la C o la V
            if (controlPulsado && evt.getKeyCode() == KeyEvent.VK_C) {
                //Como no es el control. Lo desactivamos
                controlPulsado = false;
                //Realizamos la acción
                copiarAlPortapapeles(Pantalla.getText());
            } else if (controlPulsado && evt.getKeyCode() == KeyEvent.VK_V) {
                controlPulsado = false;
                //Traemos los datos del portapapeles
                String temp = pegarDelPortapapeles();
                if (temp.length() > 0) {
                    try {
                        //Arreglamos las comas y puntos de los decimales para que los acepte java
                        temp.replace(',', '.');
                        //Si es un número lo copiamos
                        Double.parseDouble(temp);
                        //Si falla al transformarlo a número no llegará aquí y no se copiará el dato
                        Pantalla.setText(temp);
                        this.requestFocus();
                    } catch (NumberFormatException ex) {
                    }
                }
            }
        }
    }//GEN-LAST:event_formKeyPressed

