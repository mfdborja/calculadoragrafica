    private void iniciar() {
        //Portapapeles del sistema
        portapapeles = Toolkit.getDefaultToolkit().getSystemClipboard();
        //Cambio del icono de la aplicación.
        this.setIconImage((new ImageIcon("extras/icono.jpg")).getImage());
        //Esto hace que la pantalla sea lo más pequeña posible.
        this.pack();

        //Le damos el foco a la ventana para que se detecten las teclas al pulsarlas.
        //Si captura la pulsación otro elemento no sabrá que hacer.
        this.requestFocus();
    }

    void copiarAlPortapapeles(String texto) {
        //Este objeto convierte el String de java en un objeto transferible al portapapeles
        StringSelection datos = new StringSelection(texto);
        //Copia al portapepeles el objeto transferible
        portapapeles.setContents(datos, datos);
    }

    String pegarDelPortapapeles() {
        //Si no se consigue copiar el portapapeles se devolverá una cadena vacía.
        //Si es una imagen por ejemplo no nos sirve.
        String texto = "";

        //Punto intermedio entre el portapapeles y el destino
        //Para poder tratar los datos
        Transferable datosTransferibles = portapapeles.getContents(null);

        //Con esto miramos si es una cadena de texto
        if (datosTransferibles.isDataFlavorSupported(DataFlavor.stringFlavor)) {
            try {
                //Si es una cadena intentamos copiarla a un String
                texto = (String) datosTransferibles.getTransferData(DataFlavor.stringFlavor);
            } catch (UnsupportedFlavorException ex) {
            } catch (IOException ex) {
            }
        }
        //Devolvemos lo que hemos conseguido capturar.
        return texto;
    }

    Double darValorPantalla() {
        //Transforma la cadena de texto de la pantalla en un double con el que podemos operar
        String temp = Pantalla.getText();

        try {
            //Remplazamos las comas por puntos
            temp.replace(',', '.');
            //Intenta devolver el valor en pantalla
            return Double.parseDouble(temp);
        } catch (NumberFormatException ex) {
        }

        //Si hay error devuelve un valor no operable.
        return Double.NaN;
    }

    void numeroPulsado(char tecla) {
        //Recibe el valor pulsado en pantalla o por teclado y opera de acuerdo a él.
        switch (tecla) {
            case '.': {
            }//Sin break;
            //Si no se pone el break realiza un case y a continuación el siguiente hasta que encuentra un break
            //En este caso si se pulsa un punto hace nada y pasa a realizar la acción para la coma.
            //Por tanto, es indiferente pulsar uno u otro.
            case ',': {
                if (Pantalla.getText().equalsIgnoreCase("0") || Pantalla.getText().equalsIgnoreCase("0.0")) {
                    //Si la pantalla tiene un cero
                    Pantalla.setText("0.");
                } else if (Pantalla.getText().indexOf(".") == -1) {
                    //Si no hay ningún punto ya
                    Pantalla.setText(Pantalla.getText() + ".");
                }
            }
            break;

            //Sin break
            case '0':
            case '1':
            case '2':
            case '3':
            case '4':
            case '5':
            case '6':
            case '7':
            case '8':
            case '9': {
                //Si en la pantalla hay un cero lo quitamos. Los ceros a la izquierda no cuentan
                if (Pantalla.getText().equalsIgnoreCase("0")) {
                    Pantalla.setText("");
                }
                //Si detecta un número lo coge y lo añade a la pantalla por la derecha
                Pantalla.setText(Pantalla.getText() + tecla);
            }
            break;

            //Al detectar que es una operación
            //Sin break
            case '+':
            case '-':
            case '*':
            case '/': {
                //Guarda la operación en una variable para recordarla
                //Todavía no podemos operar. Solo sabermos uno de los números.                
                operacion = tecla;
                //Guarda el primer valor que nos han dado en una variable para recordarlo.
                acumulador = darValorPantalla();
                //Borra la pantalla
                Pantalla.setText("0");
            }
            break;

                //Si pulsa enter o igual realiza la operación
            case '\n':
            case '=': {
                operar();
            }
            break;

            //Borrar backspace (último valor tecleado)
            case '\b': {
                //Solo borra si hay algo en la pantalla escrito.
                //Si solo hay un cero no hace nada.
                if (!Pantalla.getText().equals("0") && Pantalla.getText().length() > 1) {
                    //Esto borra lo último escrito en la pantalla.
                    //Lo que hay en la pantalla será la cadena actual menos el último caracter
                    Pantalla.setText(Pantalla.getText().substring(0, Pantalla.getText().length() - 1));
                } else {
                    //Si la pantalla tenía un 0 o nada pinta un 0
                    Pantalla.setText("0");
                }
            }
            break;

            //Borrar todo (tecla suprimir/delete)
            case 127: {
                //Reseteamos la calculadora
                Pantalla.setText("0");
                acumulador = 0.0;
                operacion = 0;
            }
            break;


            //Valores constantes
            case 'p': {
                Pantalla.setText("3.141592653589793");
            }
            break;

            case 'e': {
                Pantalla.setText("2.718281828459045");
            }
            break;

            case 'a': {
                Pantalla.setText("1.618033988749894");
            }
            break;

            case 'u': {
                Pantalla.setText("0.577215664901532");
            }
            break;

            //Cuando no sea ninguna de las anteriores no hará nada.
            default: {
            }
            break;
        }
        
        //Poner foco en la ventana para capturar las teclas pulsadas.
        this.requestFocus();
    }

    void operar() {
        if (operacion == '+') {
            acumulador += darValorPantalla();
        } else if (operacion == '-') {
            acumulador -= darValorPantalla();
        } else if (operacion == '*') {
            acumulador *= darValorPantalla();
        } else if (operacion == '/') {
            if (darValorPantalla() != 0) {                
                acumulador /= darValorPantalla();
            } else {
                //Si divide por 0
                Pantalla.setText("Error");
            }
        }
        
        //Mostar resultado
        Pantalla.setText(String.valueOf(acumulador));


        //Resetear el signo
        operacion = 0;

        //Quitar el decimal si es cero de la pantalla
        if (Pantalla.getText().endsWith(".0")) {
            Pantalla.setText(Pantalla.getText().substring(0, Pantalla.getText().length() - 2));
        }
    }
